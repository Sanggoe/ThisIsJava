package com.sanggoe.chap4_conditionals_loops_statements;

public class ConditionalStatementExercise4 {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
